			jQuery(document).ready(function() {

				jQuery('.tabs .tab-links a').on('click', function(e) {
					var currentAttrValue = jQuery(this).attr('href');

					// Show/Hide Tabs
					jQuery('.tabs ' + currentAttrValue).fadeIn("slow").siblings().hide();

					// Change/remove current tab to active_subTab
					jQuery(this).parent('li').addClass('active_subTab').siblings().removeClass('active_subTab');

					e.preventDefault();
				});

				// Hide h1
				jQuery("#content > h1:nth-child(1)").hide();
				// Hide div.document_popup_menu
				jQuery(".document_popup_menu").hide();

			});
			
			// index
			var index = 0;
		
			var flag = true;
			
			$(".nextBtn").click(function(){
				
				if(flag) {
					
				flag = false;
				
				//만약 첫번째 이미지 보여진다면
				if(index==4) {
					index = -1;
				}
				
				var left = -(++index*800);
				
				$(".imgs").css("left",left)
   						  .one("transitionend",function() {
   						  	flag = true;
   						  });
				
				}//if end
				
			});//.nextBtn click() end
			
			$(".prevBtn").click(function(){
				
				if(flag) {
					
				flag = false;
				
				//마지막 5번째 이미지 보여진다면
				if(index == 0) {
					index = 5;
				}
				
				var left = -(--index*800);
				
				$(".imgs").css("left",left)
   						  .one("transitionend",function() {
   						  	flag = true;
   						  });
				
				}//if end
				
			});//.prevBtn click() end
			
			
